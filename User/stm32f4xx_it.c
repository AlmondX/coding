/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
		systick_time.T_bool_10ms=1;
	  systick_time.Time_60ms++;
	  systick_time.Time_1s++;
	
	if(systick_time.time_send_readdata_flag==1)
	{
			systick_time.Time_readdata_over++;
	}
	if(systick_time.Time_60ms >=6)    //下位机以周期为60ms查询数据
	{
		systick_time.Time_60ms = 0;
		systick_time.T_bool_60ms =1;
	}
	if(systick_time.Time_1s >=100)    //定时1s
	{
		systick_time.Time_1s = 0;
		systick_time.T_bool_1s =1;
	}
	TimingDelay_Decrement();
}



